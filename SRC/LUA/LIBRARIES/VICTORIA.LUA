rawset(_G, "VICTORIA", {debug = true})

VICTORIA.DebugPrint = function(str)
	if (VICTORIA.debug) print("\x89VICTORIA\x80: "..str) end
end

VICTORIA.SpawnObject = function(o, x, y, z, p, c)
	assert(o, "VICTORIA.SpawnObject() argument #1: No model to spawn!")
	x = x or 0
	y = y or 0
	z = z or 0
	p = p or 0
	c = c or 0

	assert(o[p + 1], "VICTORIA.SpawnObject(): Given model does not have pose #"..p.." !")

	local px, py, pz, pcolor

	local len = #o[p + 1]

	local spawnedmodel = {}

	for i = 1, len do
		px = o[p + 1][i][1] or 0
		py = o[p + 1][i][2] or 0
		pz = o[p + 1][i][3] or 0
		pcolor = o[p + 1][i][4] or 0

		local voxel = P_SpawnMobj(x + px*FU, y + py*FU, z + pz*FU, MT_VOXEL)
		voxel.frame = pcolor
		voxel.color = c

		table.insert(spawnedmodel, i, voxel)
	end

	VICTORIA.DebugPrint("Model spawned")
	return spawnedmodel
end

